package S1M3T1;

public class MathTest {

    public static void main(String[] args) {

        System.out.println("Max = " + Math.max(1,2));   //2
        System.out.println("Min = " + Math.min(1,2));   //1
        System.out.println("获取次方 = " + Math.pow(2,3));  //8.0
        System.out.println("获取绝对值 = " + Math.abs(-2));  //2
        System.out.println("获取四舍五入 = " + Math.round(26.6));  //27
        System.out.println("获取平方根 = " + Math.sqrt(16));  //4.0
        System.out.println("获取随机数 = " + Math.random());
    }
}
